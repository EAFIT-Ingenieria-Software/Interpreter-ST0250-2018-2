public class HundredExpression extends Expression { 
    public String five() {
      return "D";
    }
    public String four() {
      return "CD";
    }
    public String nine() {
      return "CM";
    }
    public String one() {
      return "C";
    }
    public int multiplier() {
      return 100;
    }
} 

